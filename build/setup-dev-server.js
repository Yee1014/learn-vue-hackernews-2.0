const fs = require('fs')
const path = require('path')
const MFS = require('memory-fs')
const webpack = require('webpack')
const middleware = require('webpack-dev-middleware')
const chokidar = require('chokidar')
const clientConfig = require('./webpack.client.config')
const serverConfig = require('./webpack.server.config')

const readFile = (fs, file) => {
  try {
    return fs.readFileSync(path.join(clientConfig.output.path, file), 'utf-8')
  } catch (e) {
  }
}

module.exports = function setupDevServer (app, templatePath, cb) {
  let bundle
  let template
  let clientManifest

  let ready
  const readyPromise = new Promise(r => {
    ready = r
  })
  const update = () => {
    if (bundle && clientManifest) {
      ready()
      cb(bundle, {
        template,
        clientManifest
      })
    }
  }

  // read template from disk and watch
  template = fs.readFileSync(templatePath, 'utf-8')
  chokidar.watch(templatePath).on('change', () => {
    template = fs.readFileSync(templatePath, 'utf-8')
    console.log('index.html template updated.')
    update()
  })

  // modify client config to work with hot middleware
  clientConfig.entry.app = ['webpack-hot-middleware/client', clientConfig.entry.app]
  clientConfig.output.filename = '[name].js'
  clientConfig.plugins.push(
    new webpack.HotModuleReplacementPlugin()
    // new webpack.NoEmitOnErrorsPlugin()
  )

  // dev middleware
  const clientCompiler = webpack(clientConfig, (err, stats) => {
    if (err || stats.hasErrors()) {
      // [在这里处理错误](#error-handling)
      console.log(err)
    }
  })
  const devMiddleware = middleware(clientCompiler, {
    publicPath: clientConfig.output.publicPath,
    serverSideRender: true,
  })
  app.use(devMiddleware)
  clientCompiler.run({
    // [watchOptions](/configuration/watch/#watchoptions) 示例
    aggregateTimeout: 300,
    poll: undefined
  }, (err, stats) => {
    console.log(err, stats)
    if (err) throw err
    if (!stats) return
    if (stats.hasErrors()) {
      console.error(err)
      return
    }
    stats = stats.toJson()
    stats.warnings.forEach(err => console.warn(err))
    clientManifest = JSON.parse(readFile(
      devMiddleware.outputFileSystem,
      'vue-ssr-client-manifest.json'
    ))
    update()
  })

  // hot middleware
  app.use(require('webpack-hot-middleware')(clientCompiler, { heartbeat: 5000 }))

  // watch and update server renderer
  const serverCompiler = webpack(serverConfig, (err, stats) => {
    if (err || stats.hasErrors()) {
      // [在这里处理错误](#error-handling)
      console.log(err)
    }
  })
  const mfs = new MFS()
  serverCompiler.outputFileSystem = mfs
  serverCompiler.watch({}, (err, stats) => {
    if (err) throw err
    if (!stats) return
    if (stats.hasErrors()) {
      console.error(err)
      return
    }
    stats = stats.toJson()
    if (stats.errors.length) return

    // read bundle generated by vue-ssr-webpack-plugin
    bundle = JSON.parse(readFile(mfs, 'vue-ssr-server-bundle.json'))
    update()
  })

  return readyPromise
}
